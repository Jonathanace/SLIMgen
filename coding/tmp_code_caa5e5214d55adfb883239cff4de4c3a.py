class Calculator:
    """A simple calculator class with add and subtract methods."""

    def __init__(self):
        pass

    def add(self, x, y):
        """Add two integers and return their sum.

        Args:
            x (int): The first integer to add.
            y (int): The second integer to add.

        Returns:
            int: The sum of the two integers.
        """
        return x + y

    def subtract(self, x, y):
        """Subtract one integer from another and return their difference.

        Args:
            x (int): The first integer to subtract from.
            y (int): The second integer to subtract.

        Returns:
            int: The difference between the two integers.
        """
        return x - y